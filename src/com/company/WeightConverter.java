package com.company;

public class WeightConverter {
    public static double kgToMg(double kg) {
        return kg * 1000000;
    }
    public static double kgToGram(double kg) {
        return kg * 1000;
    }
    public static double kgToTonne(double kg) {
        return kg / 1000;
    }
    public static double kgToKg(double kg) {
        return kg;
    }
    public static double kgToOunce(double kg) {
        return kg * 35.274;
    }
    public static double kgToPound(double kg) {
        return kg * 2.205;
    }
}
